#Para resolver esse problema usando apenas duas idas à sala das lâmpadas, você pode seguir este procedimento:

#Primeira visita:
#Ligue qualquer um dos interruptores por um tempo (digamos, 1 minuto) e, em seguida, desligue-o.
#Segunda visita:
#Ligue um dos outros interruptores.
#Deixe o primeiro interruptor desligado e mantenha o terceiro na posição em que foi deixado após a primeira visita.
#Agora, há três situações possíveis:

#Se a lâmpada estiver acesa quando você entrar na sala, o segundo interruptor controla essa lâmpada. 
#O primeiro e o terceiro estão associados às outras duas lâmpadas, mas você não sabe qual é qual.

#Se a lâmpada estiver apagada, mas quente ao toque, o primeiro interruptor controla essa lâmpada. 
#O segundo interruptor controla a lâmpada que você deixou ligada por um minuto durante a primeira visita,
#e o terceiro interruptor controla a lâmpada restante.

#Se a lâmpada estiver fria e desligada, o terceiro interruptor controla essa lâmpada. O primeiro interruptor 
#controla a lâmpada que você ligou durante a primeira visita, e o segundo interruptor controla a lâmpada restante.

#Dessa forma, usando apenas duas idas à sala das lâmpadas, você pode determinar qual interruptor controla cada uma delas.





